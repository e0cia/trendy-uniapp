const isNotBlank = (value) => {
    if (value === undefined || value === null || value === '' || value === 'null') {
        return false;
    } else {
        return true;
    }
};

const isBlank = (value) => {
    if (value === undefined || value === null || value === '') {
        return true;
    } else {
        return false;
    }
};

